
public class Escape {

	public static void main(String[] args) {
		System.out.println();
		System.out.println();
		System.out.println();
		System.out.println("> Fehler!!!");
		System.out.println();
		System.out.println();
		System.out.println();
		
		// Zeilenumbruch mit der Escape-Sequenz \n
		
		System.out.println();
		System.out.println();
		System.out.println();
		System.out.println("\n > Fehler!!!");
		System.out.println();
		System.out.println();
		System.out.println();
		
		// \n für neue Zeilen, \t für Tabulatoren und \" für Anführungszeichen im Text
		
		
		System.out.println("\n\n\n > Fehler!!!\n\n\n");
		System.out.println("Er sagte:\n\t Hallo Leute!");
		System.out.println("Er sagte:\n\"Bye!\n Have a great time!\"");
		

	}

}
